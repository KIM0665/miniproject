package com.kky2;


public class Posttbl {

	private String id;

	private String tale;
	
	
	public Posttbl(String id, String tale) {
		this.id = id;
		this.tale = tale;
	}



	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTale() {
		return tale;
	}

	public void setTale(String tale) {
		this.tale = tale;
	}


	
	
	
}
